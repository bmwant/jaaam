def p(n, delay, forget):
    f = [0] * n
    f[0] = 1
    for i in range(delay, n):
        start = max(0, i - forget + 1)
        end = i - delay + 1
        shared = sum(f[start:end])
        
        f[i] = shared
    #  print(f)
    mod = 10**9 + 7
    return sum(f[-forget:]) % mod


